// in javascript we need to have an initializer before declaring a variable. In ES6 we have two main types of initializers i.e:

// let : it declares values which can be changed later on.

let weight = 10.5

console.log(weight)

// const : It is used to declare variable which contains values that are not intended to be changed in entire program.
const pi = 3.142
console.log("The pi is:", pi)
